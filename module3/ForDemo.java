package module3;

public class ForDemo 
{
	public static void main(String args[])
	{
	for(int i=1;i<=10;i++)
	{
		System.out.println("Ashutosh");
		
		
	}
	System.out.println("=========================");
	int a=1;
	while(a<=10)
	{
		System.out.println("Ashutosh");
		a++;
	}
	System.out.println("=======================");
	int b=1;
	do {
		System.out.println("Ashutosh");
		b++;
		
	}while(b<=10);
	
	}
}
